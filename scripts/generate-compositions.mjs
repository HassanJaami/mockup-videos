/**
 * Reads all story.json files from public/customers/ and generates
 * src/customers.ts — a typed array used by Root.tsx to register
 * one Composition per customer in Remotion Studio.
 *
 * Run this whenever you add or update a customer:
 *   npm run gen
 */

import { existsSync, readdirSync, readFileSync, writeFileSync } from "fs";
import { resolve } from "path";
import { fileURLToPath } from "url";

const ROOT = resolve(fileURLToPath(new URL(".", import.meta.url)), "..");
const CUSTOMERS_DIR = resolve(ROOT, "public/customers");
const OUT_FILE = resolve(ROOT, "src/customers.ts");

const folders = readdirSync(CUSTOMERS_DIR, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => d.name)
  .sort();

const customers = [];

for (const folder of folders) {
  const storyPath = resolve(CUSTOMERS_DIR, folder, "story.json");
  if (!existsSync(storyPath)) continue;
  const data = JSON.parse(readFileSync(storyPath, "utf-8"));
  customers.push({ folder, data });
}

// Generate a valid Remotion composition ID from folder name
// e.g. "backlink-brain" → "BacklinkBrain"
const toCompositionId = (folder) =>
  folder
    .split("-")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join("");

const lines = [
  `// AUTO-GENERATED by scripts/generate-compositions.mjs — do not edit by hand`,
  `// Run: npm run gen`,
  `import type { CustomerStoryData } from "./CustomerStory/schema";`,
  ``,
  `export const CUSTOMERS: Array<{ id: string; folder: string; data: CustomerStoryData }> = [`,
];

for (const { folder, data } of customers) {
  const id = toCompositionId(folder);
  lines.push(`  {`);
  lines.push(`    id: ${JSON.stringify(id)},`);
  lines.push(`    folder: ${JSON.stringify(folder)},`);
  lines.push(`    data: ${JSON.stringify(data, null, 4).replace(/\n/g, "\n    ")},`);
  lines.push(`  },`);
}

lines.push(`];`);
lines.push(``);

writeFileSync(OUT_FILE, lines.join("\n"));
console.log(`Generated src/customers.ts with ${customers.length} compositions:`);
customers.forEach(({ folder }) => console.log(`  · ${toCompositionId(folder)} (${folder})`));
